public class Apartamento {
    public int numero;
    public int tamanho;
    public char bloco;
    public Boolean disponibilidade;
    public Apartamento(int numero, int tamanho, char bloco, Boolean disponibilidade) {
        this.numero = numero;
        this.tamanho = tamanho;
        this.bloco = bloco;
        this.disponibilidade = disponibilidade;
    }
    
}
